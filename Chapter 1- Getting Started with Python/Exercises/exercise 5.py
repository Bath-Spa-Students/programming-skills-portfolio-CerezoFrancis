#Exercise 5: Compute area of Circle
#Assigning the value of pi to the variable
π = 3.14  

#Taking user input for the radius of the circle and converting it to a float
Radius = float (input ("Please enter the radius of the given circle:"))  

#Calculating the area of the circle using the formula π * Radius * Radius
#and storing it in the variable 'area_of_the_circle'
area_of_the_circle = π * Radius * Radius 

#Printing the result
print (" The area of the given circle is: ", area_of_the_circle)
